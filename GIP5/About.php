            <h2>Over ons</h2>
              <p>Wij zijn Jasper T'Kindt en Kyan Vanderkelen, studenten in de richting van 6 informaticabeheer. Ons gezamenlijk GIP-project richt zich op de implementatie van een 'Info Server' voor leerlingen in informaticarichtingen. We kregen inzicht in moeilijkheden met het bewaren, updaten en verwijderen van leerlingengegevens. Ons doel is het creëren van een efficiënte info server met een gebruiksvriendelijke toegang.
                De info server zal diverse functionaliteiten omvatten, zoals gefilmde en geschreven tutorials voor leerlingen die minder bekend zijn met computers of informatica. Het beheer voor admins zal sterk geoptimaliseerd worden met diverse knoppen voor snelle aanpassingen van leerlinggegevens. Deze opdracht is voor ons beiden van groot belang en komt perfect uit door onze sterke vriendschapsband en gedeelde passie voor dit onderwerp.
                Belangrijke aspecten van ons project omvatten inlogmogelijkheden, account- en klasbeheer, tutorials en het beheer van logbestanden voor foutmonitoring. Dit project streeft naar een geïntegreerde webapplicatie die via een webbrowser toegankelijk is en verbonden is met het bestaande Dashboard.
                Ons doel is het creëren van een efficiënt systeem dat de beheerders ondersteunt in het gebruik van gegevens. Hoewel we nog lessen en uitleg zullen ontvangen met betrekking tot het koppelen van databases met code, zijn we vastberaden om aan de verwachtingen te voldoen.</p>
                
                <br>

                <h2>Onderzoeksvraag</h2>
                <p>De kern van ons project is gericht op het verbeteren van tijdsbeheer en het vereenvoudigen van taken voor administrators die de info server beheren. Leerlingen en leerkrachten krijgen de mogelijkheid om zelfstandig hun accounts te beheren, waardoor de werklast van de administrators verlicht wordt. De integratie met Smartschool maakt een gemakkelijke toegang tot het systeem mogelijk voor alle gebruikers. Belangrijke functionaliteiten omvatten accountcreatie, -wijziging, -beheer, toegankelijke tutorials en foutdetectie via logbestanden.</p>
                
                <br>

                <h2>Ontwikkelingsproces</h2>
                <p>Ons project wordt ontwikkeld met behulp van CSS, HTML, Linux, JavaScript, PHP en MD Bootstrap 5. PHP zal fungeren als de brug tussen de database en de code. De overige genoemde talen worden ingezet voor de lay-out en vormgeving van de website.
                De database zal gegevens van beheerders en leerlingen bevatten. Via SQL en PHP zullen we deze gegevens vanuit de code kunnen oproepen. Beheerders hebben de mogelijkheid om gegevens aan te passen, updaten of verwijderen. Leerlingen kunnen hun wachtwoord wijzigen en hun gegevens updaten.</p>
                
